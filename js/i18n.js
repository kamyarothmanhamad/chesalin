let currentLang = 'ru';

function t(key) {
  return TRANSLATIONS[currentLang][key] || TRANSLATIONS['ru'][key] || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    el.innerHTML = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  // Update page title and meta description
  document.title = t('meta_title');
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute('content', t('meta_desc'));
  // Update html lang attribute
  document.documentElement.lang = currentLang;
  // Update shown-count label
  const shownCount = document.getElementById('shown-count');
  if (shownCount) {
    const num = shownCount.textContent;
    const parent = document.querySelector('.gallery-stat-text');
    if (parent) parent.innerHTML = `${t('shown_photos')} <span id="shown-count">${num}</span> ${t('shown_photos2')}`;
  }
}

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('chesalin_lang', lang);
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  applyTranslations();
}

function initLanguage() {
  const saved = localStorage.getItem('chesalin_lang');
  const browserLang = navigator.language?.startsWith('ru') ? 'ru' : 'en';
  currentLang = saved || browserLang;
  setLanguage(currentLang);
}
